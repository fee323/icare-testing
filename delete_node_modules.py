import os
import shutil
import string

def get_size(path):
    total = 0
    try:
        for entry in os.scandir(path):
            try:
                if entry.is_file(follow_symlinks=False):
                    total += entry.stat().st_size
                elif entry.is_dir(follow_symlinks=False):
                    total += get_size(entry.path)
            except:
                pass
    except:
        pass
    return total

def find_and_delete_node_modules(root_path, deleted_list):
    try:
        for entry in os.scandir(root_path):
            try:
                if entry.is_dir(follow_symlinks=False):
                    if entry.name == "node_modules":
                        size = get_size(entry.path)
                        size_gb = size / (1024**3)
                        print(f"\nFound: {entry.path}")
                        print(f"Size: {size_gb:.2f} GB")
                        
                        try:
                            shutil.rmtree(entry.path)
                            print(f"✓ Deleted!")
                            deleted_list.append((entry.path, size_gb))
                        except Exception as e:
                            print(f"✗ Failed: {e}")
                    else:
                        # Skip system folders
                        skip_folders = {'Windows', 'Program Files', 'Program Files (x86)', 
                                      'ProgramData', '$Recycle.Bin', 'System Volume Information',
                                      'Recovery', 'PerfLogs', 'Boot', 'AppData'}
                        if entry.name not in skip_folders:
                            find_and_delete_node_modules(entry.path, deleted_list)
            except PermissionError:
                pass
            except:
                pass
    except PermissionError:
        pass
    except:
        pass

print("="*60)
print("NODE_MODULES CLEANER")
print("="*60)

# Scan drives
drives_to_scan = []
for letter in string.ascii_uppercase:
    drive = f"{letter}:\\"
    if os.path.exists(drive):
        drives_to_scan.append(drive)

print(f"\nAvailable drives: {', '.join(drives_to_scan)}")
print("\nEnter drives to scan (e.g., C D) or press Enter for all:")
user_input = input("> ").strip().upper()

if user_input:
    selected = [f"{d}:\\" for d in user_input.split() if f"{d}:\\" in drives_to_scan]
else:
    selected = drives_to_scan

print(f"\nScanning: {', '.join(selected)}")
print("This may take a few minutes...\n")

deleted = []

for drive in selected:
    print(f"\n{'='*60}")
    print(f"Scanning {drive}")
    print(f"{'='*60}")
    find_and_delete_node_modules(drive, deleted)

print(f"\n{'='*60}")
print("SUMMARY")
print(f"{'='*60}")

if deleted:
    total_freed = sum(size for _, size in deleted)
    print(f"\nTotal node_modules deleted: {len(deleted)}")
    print(f"Total space freed: {total_freed:.2f} GB\n")
    
    print("Deleted folders:")
    for path, size in deleted:
        print(f"  - {path} ({size:.2f} GB)")
else:
    print("\nNo node_modules folders found or deleted.")

print(f"\n{'='*60}")
