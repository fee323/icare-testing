  static List<Map<String, String>> getCodesByCategory(String category) {
    return categories[category] ?? [];
  }

  static List<Map<String, String>> searchCodes(String query) {
    if (query.isEmpty) return [];

    final results = <Map<String, String>>[];
    final lowerQuery = query.toLowerCase();

    categories.forEach((category, codes) {
      for (var code in codes) {
        if (code['code']!.toLowerCase().contains(lowerQuery) ||
            code['description']!.toLowerCase().contains(lowerQuery) ||
            category.toLowerCase().contains(lowerQuery)) {
          results.add({
            'code': code['code']!,
            'description': code['description']!,
            'category': category,
          });
        }
      }
    });

    return results;
  }
